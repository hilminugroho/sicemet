    <section id="about" class="about">
      <div class="container" data-aos="fade-up">

        <div class="section-title">
          <h2>Apa itu Metodologi V shaped Model?</h2>
        </div>
        <div class="col-lg-6 order-1 order-lg-2 hero-img" data-aos="zoom-in" data-aos-delay="200" style="margin-left: auto; margin-right: auto; display: block;">
          <img src="assets/img/model/alur_model_shape.png" class="img-fluid animated" alt="">
        </div>
        <hr>
        <div class="row content">
          <div class="col-lg-6 pt-4 pt-lg-0">

            <p> <b>Definisi</b>
            </p>
            <p>
            Model V adalah model pengembangan perangkat lunak yang didasarkan pada hubungan antara setiap fase pengembangan siklus hidup yang tercantum dalam model Waterfall yang merupakan pengembangan perangkat lunak dan fase yang terkait pengujian. Sama seperti model air terjun, V- yang siklus hidup berbentuk jalan berurutan dari pelaksanaan proses. Setiap fase harus diselesaikan sebelum tahap berikutnya dimulai. Pengujian ditekankan dalam model ini lebih dari model air terjun. 
            </p>
            <p>
              <b>Kelebihan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> V model sangat fleksibel. Model ini bisa digunakan untuk project tailoring serta penambahan pengurangan method dan tool secara dinamik.</li>
              <li><i class="ri-check-double-line"></i> V model dikembangkan dan di maintain oleh publik. User dari V model berpartisipasi dalam change board yang memproses semua change request terhadap V model.</li>
              <li><i class="ri-check-double-line"></i> Penyesuaian yang cepat pada projek yang baru.</li>
              <li><i class="ri-check-double-line"></i> 	Memudahkan dalam pembuatan dokumen projek.</li>
              <li><i class="ri-check-double-line"></i> Biaya yang murah dalam perawatan dan modifikasinya.</li>
            </ul>
            <p>
              <b>Kekurangan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> V model hanya bisa digunakan sekali dalam suatu proyek hal tersebut disebabkan kerena  model ini merupakan model yang project oriented.</li>
              <li><i class="ri-check-double-line"></i> V model bersifat terlalu fleksibel sehingga mengakibatkan beberapa aktivitas-aktivitas yang digambarkan dalam V model menjadi terlalu abstrak. </li>
              <li><i class="ri-check-double-line"></i> Aktifitas V-Model hanya difokuskan pada projectnya saja, bukan pada keseluruhan organisasi.</li>
              <li><i class="ri-check-double-line"></i> Prosesnya hanya secara sementara. Ketika project selesai, jalannya proses model dihentikan. Tidak berlangsung untuk keseluruhan organisasi.</li>
              <li><i class="ri-check-double-line"></i> Metode yang ditawarkan terbatas. Sehingga kita tidak memiliki cara pandang dari metode yang lain. </li>
              <li><i class="ri-check-double-line"></i> Toolnya tidak selengkap yang dibicarakan SDE (Software Development Environment). Tidak ada tools untuk hardware di V-Model. </li>
            </ul>
            <p>
              <b>Metodologi V Shape Model cocok digunakan ketika</b>
            </p>
            <ul>
            <li><i class="ri-check-double-line"></i> System yang semua persyaratannya diketahui.</li>
            <li><i class="ri-check-double-line"></i> Solusi dan teknologinya telah jelas diketahui.</li>
            <li><i class="ri-check-double-line"></i> System yang menuntut kehandalan tinggi.</li>
            <li><i class="ri-check-double-line"></i> Bisa dimodifikasi untuk menyesuaikan dengan perubahan setelah fase analisis.</li>
          </ul>
          </div>
           <div class="col-lg-6">
            <p>
              <b>Tahapan pada model V Shape</b> 
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Business Case
                <p>Merupakan tahapan awal yang menggambarkan kebutuhan/harapan konsumen terhadap sistem yang akan dikembangkan, termasuk manfaat sistem terhadap konsumen dan perkiraan biaya yang harus disediakan.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Requirement
                <p>Tahap perencanaan meliputi estimasi biaya yang digunakan, batas waktu, pengaturan jadwal, identifikasi lingkungan kerja, sumber-sumber informasi untuk melakukan iterasi (Teknik perulangan). Hasil dari tahapan ini adalah dokumen spesifikasi kebutuhan sistem dan bisnis.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Analisis Informasi
                <p>Setelah diperoleh spesifikasi sistem dari fase requirement, selanjutnya aktivitas difokuskan bagaimana cara kerja software untuk memenuhi kebutuhan tersebut, termasuk metode, hardware dan software apa saja yang diperlukan untuk mencapai kebutuhan yang sudah didefinisikan.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Perancangan Sistem
                <p> Pada tahapan ini akan dibuat rancangan software secara lebih terinci sesuai spesifikasi yang sudah disepakati.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Unit Designed
                <p> Merancang setiap elemen/unit software termasuk rancangan modul/program, antarmuka, database dan lain-lain.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Development
                <p> Merealisasikan hasil rancangan menjadi satu aplikasi/program tertentu.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Unit Test
                <p> Menguji setiap komponen/unit program apakah sesuai dengan rancangan unit yang sudah ditetapkan. Secara teoritis seharusnya pengujian dilakukan oleh orang tertentu yang bertugas sebagai software tester, tetapi dalam kenyataannya seringkali unit testing dilakukan oleh programmer sendiri.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Acceptance Test
                <p> Merupakan aktivitas untuk menguji sejauh mana sistem/software dapat membantu memecahkan business case, dalam artian apakah sistem/software tersebut sudah sesuai dengan harapan konsumen/klien dan sejauh mana manfaat sistem/software ini bagi klien. Test ini sering kali disebut sebagai User Acceptance Test (UAT).</p>
              </li>
              <li><i class="ri-check-double-line"></i> Release Testing
                <p>Test ini dilakukan untuk menguji sejauh mana sistem/software dapat mendukung aktivitas organisasi dan berjalan dengan harmonis sesuai dengan kegiatan rutin organisasi. </p>
              </li>
            </ul>
