        <div class="section-title">
          <h2>Apa itu Metodologi RAD?</h2>
        </div>
        <div class="col-lg-6 order-1 order-lg-2 hero-img" data-aos="zoom-in" data-aos-delay="200" style="margin-left: auto; margin-right: auto; display: block;">
          <img src="assets/img/model/alur_rad.jpg" class="img-fluid animated" alt="">
        </div>
        <hr>
        <div class="row content">
          <div class="col-lg-6 pt-4 pt-lg-0">

            <p><b>Definisi</b>
            </p>
            <p>RAD adalah proses pengembangan perangkat lunak yang semakin meningkatkan (incremental) yang menekankan pada siklus pengembangan yang cepat. Model RAD merupakan adaptasi “berkecepatan tinggi” dari linear sequential model dimana pengembangan yang cepat dapat diperoleh dengan menggunakan pembangunan berbasis pada komponen. RAD, model proses perkembangan perangkat lunak sekuensial linier yang menekankan siklus perkembangan yang sangat pendek (kira-kira 60 sampai 90 hari). 
            </p>
            <p>RAD harus didukung oleh perangkat dan lingkungan pengembangan yang memadai, biasanya dikembangkan berdasarkan orientasi komponen. Lingkungan pengembangan telah memiliki library yang luar biasa besar dan lengkap seperti Java Development Kit beserta seluluru library dari Sun dan vendor-vendor lain pendukung.
            </p>
            <p>
              <b>Kelebihan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Mengembangkan sendiri.</li>
              <li><i class="ri-check-double-line"></i> Proses pengiriman menjadi lebih mudah, hal ini dikarenakan proses pembuatan lebih banyak menggunakan potongan-potongan script.</li>
              <li><i class="ri-check-double-line"></i> Mudah untuk diamati karena menggunakan model prototype, sehingga user lebih mengerti akan sistem yang dikembangkan.</li>
              <li><i class="ri-check-double-line"></i> 	Lebih fleksibel karena pengembang dapat melakukan proses desain ulang pada saat yang bersamaan.</li>
              <li><i class="ri-check-double-line"></i> Bisa mengurangi penulisan kode yang kompleks karena menggunakan wizard.</li>
              <li><i class="ri-check-double-line"></i> Keterlibatan user semakin meningkat karena merupakan bagian dari tim secara keseluruhan.</li>
              <li><i class="ri-check-double-line"></i> Mampu meminimalkan kesalahan-kesalahan dengan menggunakan alat-alat bantuan (CASE tools).<
              <li><i class="ri-check-double-line"></i> Mempercepat waktu pengembangan sistem secara keseluruhan karena cenderung mengabaikan kualitas.</li>
              <li><i class="ri-check-double-line"></i> Tampilan yang lebih standar dan nyaman dengan bantuan software-software pendukung.</li>
            </ul>
            <p>
              <b>Kekurangan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Dengan melakukan pembelian belum tentu bisa menghemat biaya dibandingkan dengan mengembangkan sendiri.</li>
              <li><i class="ri-check-double-line"></i> Membutuhkan biaya tersendiri untuk membeli peralatan-peralatan penunjang seperti misalnya software dan hardware.</li>
              <li><i class="ri-check-double-line"></i> Kesulitan melakukan pengukuran mengenai kemajuan proses.</li>
              <li><i class="ri-check-double-line"></i> Kurang efisien karena apabila melakukan pengkodean dengan menggunakan tangan bisa lebih efisien.</li>
              <li><i class="ri-check-double-line"></i> Ketelitian menjadi berkurang karena tidak menggunakan metode yang formal dalam melakukan pengkodean.</li>
              <li><i class="ri-check-double-line"></i> Lebih banyak terjadi kesalahan apabila hanya mengutamakan kecepatan dibandingkan dengan biaya dan kualitas.</li>
              <li><i class="ri-check-double-line"></i> Fasilitas-fasilitas banyak yang dikurangi karena terbatasnya waktu yang tersedia.</li>
              <li><i class="ri-check-double-line"></i> Sistem sulit diaplikasikan di tempat yang lain.</li>
              <li><i class="ri-check-double-line"></i> Fasilitas yang tidak perlu terkadang harus disertakan, karena menggunakan komponen yang sudah jadi, sehingga hal ini membuat biaya semakin meningkat.</li>
            </ul>
          </div>
           <div class="col-lg-6">
            <p>
              <b>Tahapan pada model RAD</b> 
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> equirements Planning (Perencanaan Syarat-Syarat)
                <p>Dalam fase ini, pengguna dan penganalisis bertemu untuk mengidentifikasikan tujuan-tujuan aplikasi atau sistem serta untuk megidentifikasikan syarat-syarat informasi yang ditimbulkan dari tujuan-tujuan tersebut. Orientasi dalam fase ini adalah menyelesaikan masalah-masalah perusahaan. Meskipun teknologi informasi dan sistem bisa mengarahkan sebagian dari sistem yang diajukan, fokusnya akan selalu tetap pada upaya pencapaian tujuan-tujuan perusahaan.</p>
              </li>
              <li><i class="ri-check-double-line"></i> RAD Design Workshop (Workshop Desain RAD)
                <p>Fase ini adalah fase untuk merancang dan memperbaiki yang bisa digambarkan sebagai workshop. Penganalisis dan dan pemrogram dapat bekerja membangun dan menunjukkan representasi visual desain dan pola kerja kepada pengguna. Workshop desain ini dapat dilakukan selama beberapa hari tergantung dari ukuran aplikasi yang akan dikembangkan. Selama workshop desain RAD, pengguna merespon prototipe yang ada dan penganalisis memperbaiki modul-modul yang dirancang berdasarkan respon pengguna. Apabila sorang pengembangnya merupakan pengembang atau pengguna yang berpengalaman, Kendall menilai bahwa usaha kreatif ini dapat mendorong pengembangan sampai pada tingkat terakselerasi.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Implementation (Implementasi)
                <p>Pada fase implementasi ini, penganalisis bekerja dengan para pengguna secara intens selama workshop dan merancang aspek-aspek bisnis dan nonteknis perusahaan. Segera setelah aspek-aspek ini disetujui dan sistem-sistem dibangun dan disaring, sistem-sistem baru atau bagian dari sistem diujicoba dan kemudian diperkenalkan kepada organisasi.</p>
              </li>
            </ul>
            <p>
              <b>Metodologi RAD cocok digunakan ketika</b>
            </p>
            <ul>
            <li><i class="ri-check-double-line"></i> Aplikasi akan dijalankan standalone.</li>
            <li><i class="ri-check-double-line"></i> Banyak menggunakan komponen yang ada.</li>
            <li><i class="ri-check-double-line"></i>Kinerja bukan sesuatu yang kritis.</li>
            <li><i class="ri-check-double-line"></i> Distribusi produk sempit.</li>
            <li><i class="ri-check-double-line"></i> Scope proyek sangat dibatasi.</li>
            <li><i class="ri-check-double-line"></i> Reliability tidak kritis.</li>
            <li><i class="ri-check-double-line"></i> Sistem dapat dipecah menjadi beberapa modul.</li>
            <li><i class="ri-check-double-line"></i> Sasaran produk adalah pasar yang sangat khusus.</li>
            <li><i class="ri-check-double-line"></i> Proyek memiliki jadwal yang ketat.</li>
            <li><i class="ri-check-double-line"></i> Teknologi yang digunakan lebih dari satu tahun.</li>
          </ul>
