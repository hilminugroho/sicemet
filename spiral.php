    <section id="about" class="about">
      <div class="container" data-aos="fade-up">

        <div class="section-title">
          <h2>Apa itu Metodologi Spiral?</h2>
        </div>
        <div class="col-lg-6 order-1 order-lg-2 hero-img" data-aos="zoom-in" data-aos-delay="200" style="margin-left: auto; margin-right: auto; display: block;">
          <img src="assets/img/model/alur_spiral.png" class="img-fluid animated" alt="">
        </div>
        <hr>
        <div class="row content">
          <div class="col-lg-6 pt-4 pt-lg-0">

            <p> <b>Definisi</b>
            </p>
            <p>
              Model Spiral merupakan model proses perangkat lunak yang memadukan wujud pengulangan dari model prototyping dengan aspek pengendalian dan sistematika dari linear sequential model, dengan penambahan elemen baru yaitu analisis resiko. Model Spiral ini juga  adalah salah satu metode yang dapat digunakan dalam pengembangan perangkat lunak yang merupakan penggabungan dari model prototyping dan model waterfall. Model prototyping yang fokus pada penyajian atau presentasi kepada user dengan format input dan output kemudian perangkat lunak akan dievaluasi. Model waterfall yang fokus kepada proses pengembangan perangkat lunak yang sistematis atau berurutan serta  menekankan pada analisa resiko setiap tahapannya. 
            </p>
            <p>
              <b>Kelebihan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Pembangunan dan perubahan perangkat lunak yang terjadi dapat diselesaikan secara sistematis.</li>
              <li><i class="ri-check-double-line"></i> Mudah dalam mengestimasi biaya karena proses pembuatan prototype yang jelas dan terencana dalam tahapan yang sistematis.</li>
              <li><i class="ri-check-double-line"></i> Manajemen dan analisa risiko yang lebih cepat dan mudah.</li>
              <li><i class="ri-check-double-line"></i> Mudah dalam melakukan perubahan kebutuhan dan dokumentasi.</li>
              <li><i class="ri-check-double-line"></i> Produksi software bisa terjadi lebih cepat.</li>
            </ul>
            <p>
              <b>Kekurangan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Tidak cocok dan sulit diimplementasikan dalam projek kecil.</li>
              <li><i class="ri-check-double-line"></i> Memakan waktu yang cukup lama.</li>
              <li><i class="ri-check-double-line"></i> Membutuhkan best practice atau pengalaman sebelumnya karena proses yang sangat kompleks.</li>
              <li><i class="ri-check-double-line"></i> Resiko dalam tahap planning cukup besar. Misalnya terjadi perbedaan dalam jadwal pengembangan dan anggaran belanja.</li>
            </ul>
            <p>
              <b>Metodologi Spiral cocok digunakan ketika</b>
            </p>
            <p>
                Cocok digunakan untuk pengembangan sistem skala besar dimana pengembang lebih berhati-hati di setiap tahapannya sehingga meminimalisir terjadinya kesalahan dan juga ketika metode spiral ini digunakan diharapkan memiliki waktu pengerjaan serta dana yang cukup.
            </p>
          </div>
           <div class="col-lg-6">
            <p>
              <b>Tahapan pada model Spiral</b> 
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Liason
                <p>Tahap ini berhubungan dengan komunikasi antara pihak-pihak yang terlibat dalam pengembangan softaware (seperti: system analyst) dengan pelanggan (user). Tujuannya adalah memperbaiki dan mengembangan software sesuai kebutuhan dan keinginan hingga memuaskan pelanggan.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Planning
                <p>Tahap perencanaan meliputi estimasi biaya yang digunakan, batas waktu, pengaturan jadwal, identifikasi lingkungan kerja, sumber-sumber informasi untuk melakukan iterasi (Teknik perulangan). Hasil dari tahapan ini adalah dokumen spesifikasi kebutuhan sistem dan bisnis.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Analisis Risiko
                <p>Tahap analisis resiko ini berfungsi untuk mengidentifikasi resiko yang berpotensi akan terjadi dan menghasilkan solusi alternatif secara teknis dan manajemen saat strategi mitigasi (upaya untuk mengurangi resiko bencana) direncanakan dan diselesaikan.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Rekayasa (engineering)
                <ol>
                    <li> Menguji, coding dan mengembangkan software</li>
                    <li>Menginstal software</li>
                    <li>Membuat prototype</li>
                    <li>Mendesain dokumen</li>
                    <li>Meringkas suatu pengujian software</li>
                    <li>Membuat laporan kekurangan dari software agar segera diperbaiki</li>
                </ol>
              </li>
              <li><i class="ri-check-double-line"></i> Evaluasi
                <p>Pada tahap evaluasi, system analyst membutuhkan masukan dan tanggapan dari para user dalam mengevaluasi perangkat/produk yang diuji dan memastikan bahwa produk dibutuhkan sesuai ketentuan yang telah dibicarakan diawal dengan user. System analyst memastikan pelanggan puas dengan produk yang akan dihasilkan untuk menjawab persoalan bisnis mereka. Selain itu, system analyst harus tetap memantau resiko yang akan terjadi seperti faktor-faktor yang dapat menyebabkan cost overrun (pembengkakan biaya).</p>
              </li>
            </ul>
          </div>
