    <section id="about" class="about">
      <div class="container" data-aos="fade-up">

        <div class="section-title">
          <h2>Apa itu Metodologi Prototyping?</h2>
        </div>
        <div class="col-lg-6 order-1 order-lg-2 hero-img" data-aos="zoom-in" data-aos-delay="200" style="margin-left: auto; margin-right: auto; display: block;">
          <img src="assets/img/model/alur_prototyping.png" class="img-fluid animated" alt="">
        </div>
        <hr>
        <div class="row content">
          <div class="col-lg-6 pt-4 pt-lg-0">

            <p> <b>Definisi</b>
            </p>
            <p>
            Proses pengembangan sistem seringkali menggunakan pendekatan prototipe (prototyping). Metode ini sangat baik digunakan untuk menyelesaikan masalah kesalahpahaman antara user dan analis yang timbul akibat user tidak mampu mendefinisikan secara jelas kebutuhannya (Mulyanto, 2009). Prototyping adalah pengembangan yang cepat dan pengujian terhadap model kerja (prototipe) dari aplikasi baru melalui proses interaksi dan berulang ulang yang biasa digunakan ahli sistem informasi dan ahli bisnis. Prototyping disebut juga desain aplikasi cepat (rapid application design/RAD) karena menyederhanakan dan mempercepat desain sistem (Oâ€™brien, 2005). Sebagian user kesulitan mengungkapkan keinginannya untuk mendapatkan aplikasi yang sesuai dengan kebutuhannya. kesulitan ini yang perlu diselesaikan oleh analis dengan memahami kebutuhan user dan menerjemahkannya ke dalam bentuk model (prototype). Model ini selanjutnya diperbaiki secara terus menerus sampai sesuai dengan kebutuhan user. 
            </p>
            <p>
              <b>Kelebihan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Adanya komunikasi yang baik antara pengembang dan pelanggan.</li>
              <li><i class="ri-check-double-line"></i> Pengembang dapat bekerja lebih baik dalam menentukan kebutuhan pelanggan.</li>
              <li><i class="ri-check-double-line"></i> Pelanggan berperan aktif dalam pengembangan sistem.</li>
              <li><i class="ri-check-double-line"></i> 	Lebih menghemat waktu dalam pengembangan sistem.</li>
              <li><i class="ri-check-double-line"></i> Penerapan menjadi lebih mudah karena pemakai mengetahui apa yang diharapkannya.</li>
            </ul>
            <p>
              <b>Kekurangan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Pelanggan tidak melihat bahwa perangkat lunak belum mencerminkan kualitas perangkat lunak secara keseluruhan dan belum memikirkan pemeliharaan dalam jangka waktu yang lama.</li>
              <li><i class="ri-check-double-line"></i> Pengembang biasanya ingin cepat menyelesaikan proyek sehingga menggunakan algoritma dan bahasa pemrograman sederhana.</li>
              <li><i class="ri-check-double-line"></i> Hubungan pelanggan dengan komputer mungkin tidak menggambarkan teknik perancangan yang baik.</li>
            </ul>
          </div>
           <div class="col-lg-6">
            <p>
              <b>Tahapan pada model Prototyping</b> 
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Pengumpulan Kebutuhan Pada tahap pengumpulan kebutuhan, Pelanggan dan pengembang bersama-sama mendefinisikan format dan kebutuhan kesseluruhan perangkat lunak, mengidentifikasikan semua kebutuhan, dan garis besar sistem yang akan dibuat.
              </li>
              <li><i class="ri-check-double-line"></i> Membangun Prototyping Pada tahap pembangunan prototyping, pelanggan dan pembuat sistem bersama-sama membuat format input maupun output yang akan dihasilkan oleh sistem yang dibuat.
              </li>
              <li><i class="ri-check-double-line"></i> Evaluasi Prototyping Selanjutnya, setelah tahap pembangunan prototyping, Pelanggan dan pengembang bersama-sama mendefinisikan format dan kebutuhan kesseluruhan perangkat lunak, mengidentifikasikan semua kebutuhan, dan garis besar sistem yang akan dibuat.
              </li>
              <li><i class="ri-check-double-line"></i> Mengkodekan System Dalam tahap ini prototyping yang sudah disepakati diterjemahkan ke dalam bahasa pemrograman yang sesuai.
              </li>
              <li><i class="ri-check-double-line"></i> Menguji System Pada tahap pengujian system, koding yang telah dibuat sebelumnya akan diuji apakah dapat berjalan dengan baik ataukan masih ada bagian-bagian yang perlu diperbaiki atau apakah masih ada bagian yang belum sesuai dengan keinginan pelanggan.
              </li>
              <li><i class="ri-check-double-line"></i> Evaluasi System Evaluasi system bukanlah evaluasi prototyping, evaluasi system adalah mengevaluasi system atau perangkat lunak yang sudah jadi apakah sudah sesuai dengan keinginan pelanggan atau belum. Jika belum, maka system akan direvisi kembali dan kembali ketahap 4 dan 5. Jika system sudah dikatakan OK maka system siap dilanjutkan pada tahap selanjutnya.
              </li>
              <li><i class="ri-check-double-line"></i>Menggunakan System Tahap ini merupakan tahap akhir dari pembuatan system dengan metode Prototyping Model. Pada tahap ini perangkat lunak yang sudah jadi dan sudah lulus uji, siap untuk digunakan oleh pelanggan/user.
              </li>
            </ul>
            <p>
              <b>Metodologi Prototyping cocok digunakan ketika</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Client tidak mengetahui ekspektasi sistem yang akan dibuat.</li>
              <li><i class="ri-check-double-line"></i> Kebutuhan kurang jelas.</li>
              <li><i class="ri-check-double-line"></i> Waktu proyek yang panjang.</li>
            </ul>
          </div>
