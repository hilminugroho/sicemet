    <section id="about" class="about">
      <div class="container" data-aos="fade-up">

        <div class="section-title">
          <h2>Apa itu Metodologi XP?</h2>
        </div>
        <div class="col-lg-6 order-1 order-lg-2 hero-img" data-aos="zoom-in" data-aos-delay="200" style="margin-left: auto; margin-right: auto; display: block;">
          <img src="assets/img/model/alur_xp.png" class="img-fluid animated" alt="">
        </div>
        <hr>
        <div class="row content">
          <div class="col-lg-6 pt-4 pt-lg-0">

            <p> <b>Definisi</b>
            </p>
            <p>“Extreme Programming (XP) adalah metode pengembangan software yang cepat, efisien, beresiko rendah, fleksibel, terprediksi, scientific, dan menyenangkan.“</p>
            <p>Model ini cenderung menggunakan pendekatan Object-Oriented. Tahapan-tahapan yang harus dilalui antara lain: Planning, Design, Coding, dan Testing. Sasaran Extreme Programming adalah tim yang dibentuk berukuran antara kecil sampai medium saja, tidak perlu menggunakan sebuah tim yang besar. Hal ini dimaksudkan untuk menghadapi requirements yang tidak jelas maupun terjadinya perubahan-perubahan requirements yang sangat cepat. Extreme Programming merupakan agile methods yang paling banyak digunakan dan menjadi sebuah pendekatan yang sangat terkenal. 
            </p>
            <p>
              <b>Kelebihan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Meningkatkan kepuasan kepada klien.</li>
              <li><i class="ri-check-double-line"></i> Pembangunan sistem dibuat lebih cepat.</li>
              <li><i class="ri-check-double-line"></i> Menjalin komunikasi yang baik dengan client.</li>
              <li><i class="ri-check-double-line"></i> Meningkatkan komunikasi dan sifat saling menghargai antar developer.</li>
              <li><i class="ri-check-double-line"></i> Men-delivery produk dengan cepat dan tepat.</li>
              <li><i class="ri-check-double-line"></i> Prioritas tertinggi pada pelayanan sistem adalah yang paling diuji.</li>
            </ul>
            <p>
              <b>Kekurangan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Stories yang menunjukkan requirements dari pelanggan kemungkinan besar tidak lengkap sehingga Developer harus selalu siap dengan perubahan karena perubahan akan selalu diterima.</li>
              <li><i class="ri-check-double-line"></i> Tidak bisa membuat kode yang detail di awal (prinsip simplicity dan juga anjuran untuk melakukan apa yang diperlukan hari itu juga).</li>
              <li><i class="ri-check-double-line"></i> XP tidak memiliki dokumentasi formal yang dibuat selama pengembangan. Satu-satunya dokumentasi adalah dokumentasi awal yang dilakukan oleh user.</li>
            </ul>
            <p>
              <b>Metodologi XP cocok digunakan ketika</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Membutuhkan perubahan yang cepat (misalnya: Game Mobile).</li>
              <li><i class="ri-check-double-line"></i> Proyek beresiko tinggi dengan tantangan yang berat.</li>
              <li><i class="ri-check-double-line"></i> Tim programmer sedikit, yaitu sekitar 2–10 orang namun berpengalaman semua.</li>
              <li><i class="ri-check-double-line"></i> Adanya permintaan dari pelanggan secara langsung.</li>
              <li><i class="ri-check-double-line"></i> Kebutuhan tidak jelas diawal.</li>
              <li><i class="ri-check-double-line"></i> Sistem yang akan dibuat urgent.</li>
            </ul>
            
          </div>
           <div class="col-lg-6">
            <p>
              <b>Tahapan pada model XP</b> 
            </p>
            <p>Aktivitas planning dimulai dengan membentuk user stories. Anggota XP team kemudian menilai setiap story dan menentukan cost — diukur dalam development week.</p>
            <ul>
            <li><i class="ri-check-double-line"></i>Planning
              <p>Customer dan XP team bekerja bersama untuk memutuskan bagaimana grup story untuk release berikutnya (software increment berikutnya) untuk dibangun oleh XP team. Jika komitmen telah dibuat, XP team akan membangun story-story dengan cara :</p>
              <p>Semua story segera diimplemetasikan (dalam beberapa minggu)</p>
                <ul>
                  <li><i class="ri-check-double-line"></i> Story dengan value tertinggi akan dipindahkan dari jadwal dan dimplementasikan pertama.
                  </li>
                  <li><i class="ri-check-double-line"></i> Story dengan resiko paling tinggi akan diimplemetasikan terlebih dulu. Setelah project pertama direlease dan didelivery, XP team memperhitungkan kecepatan project. Selama development, customer dapat menambah story, merubah value, membagi story atau menghapusnya.</li>
                </ul>
              </li>
            </ul>
            <ul>
                <li><i class="ri-check-double-line"></i> Desain
                  <p>XP menggunakan CRC card, untuk mengenali dan mengatur object oriented class yang sesuai dengan software increment.</p>
                </li>
                <li><i class="ri-check-double-line"></i> Coding
                  <p>Sebelum membuat code, lebih baik membuat unit test tiap story untuk dimasukkan dalam software increment. XP menyarankan agar dua orang bekerja bersama pada satu komputer workstation untuk membuat code dari satu story (pair programming), untuk menyediakan real time problem solving dan jaminan real time quality. Setelah pair programming selesai, code diintegrasikan dengan kerja lainnya (continuous integration).</p>
                </li>
                <li><i class="ri-check-double-line"></i> Testing
                  <p>Unit test yang telah dibuat harus diimplementasikan menggunakan suatu framework dan diatur ke dalam universal testing suite, integrasi dan validasi sistem dapat dilakukan setiap hari. Customer test (acceptance test) dilakukan oleh customer dan fokus pada keseluruhan fitur dan fungsional sistem. Acceptance test diperoleh dari customer stories yang telah diimplemetasikan sebagai bagian dari software release.</p>
                </li>
            </ul>
