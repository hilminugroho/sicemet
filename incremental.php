    <section id="about" class="about">
      <div class="container" data-aos="fade-up">

        <div class="section-title">
          <h2>Apa itu Metodologi Incremental?</h2>
        </div>
        <div class="col-lg-6 order-1 order-lg-2 hero-img" data-aos="zoom-in" data-aos-delay="200" style="margin-left: auto; margin-right: auto; display: block;">
          <img src="assets/img/model/alur_incremental2.png" class="img-fluid animated" alt="">
        </div>
        <hr>
        <div class="row content">
          <div class="col-lg-6 pt-4 pt-lg-0">

            <p> <b>Definisi</b>
            </p>
            <p>
            Metode Incremental merupakan salah satu metode pengembangan perangkat lunak yang mampu meminimalisir ketidaksesuaian dalam proses pengembangan perangkat lunak dan model pengembangannya secara bertahap. Pada metode increment, setiap tahapan yang ada dalam metodologi terdapat masukan (input) dan keluaran (output). Output dari proses increment akan dijadikan sebagai masukan (input) untuk proses increment selanjutnya. Dengan menggunakan incremental model dapat membantu kita untuk mengurangi biaya sebelum mencapai level dari initial productivity dan mengakselerasi proses dari pembuatan suatu fungsi sistem. Incremental model menggambarkan suatu proses dimana mengutamakan perhatian pada system requirement dan mengimplementasinya dalam team development. Model Incremental merupakan hasil kombinasi elemen-elemen dari model waterfall yang diaplikasikan secara berulang, atau bisa disebut gabungan dari Model linear sekuensial (waterfall) dengan Model Prototype. 
            </p>
            <p>
              <b>Kelebihan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Merupakan model dengan manajemen yang sederhana.</li>
              <li><i class="ri-check-double-line"></i> Pengguna tidak perlu menunggu sampai seluruh sistem dikirim untuk mengambil keuntungan dari sistem tersebut. Increment yang pertama sudah memenuhi persyaratan mereka yang paling kritis, sehingga perangkat lunak dapat segera digunakan.</li>
              <li><i class="ri-check-double-line"></i> Resiko untuk kegagalan proyek secara keseluruhan lebih rendah. Walaupun masalah masih dapat ditemukan pada beberapa increment. Karena layanan dengan prioritas tertinggi diserahkan pertama dan increment berikutnya diintegrasikan dengannya, sangatlah penting bahwa layanan sistem yang paling penting mengalami pengujian yang ketat. Ini berarti bahwa pengguna akan memiliki kemungkinan kecil untuk memenuhi kegagalan perangkat lunak pada increment sistem yang paling bawah.</li>
              <li><i class="ri-check-double-line"></i> Nilai penggunaan dapat ditentukan pada setiap increment sehingga fungsionalitas sistem disediakan lebih awal.</li>
              <li><i class="ri-check-double-line"></i> Memiliki risiko lebih rendah terhadap keseluruhan pengembagan sistem.</li>
              <li><i class="ri-check-double-line"></i> Prioritas tertinggi pada pelayanan sistem adalah yang paling diuji.</li>
            </ul>
            
          </div>
           <div class="col-lg-6">
            <p>
              <b>Tahapan pada model Incremental</b> 
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Requirement
                <p>proses penentuan kebutuhan atau analisis kebutuhan.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Specification
                <p>proses spesifikasi dimana menggunakan analisis kebutuhan    
                 sebagai acuannya.
                </p>
              </li>
              <li><i class="ri-check-double-line"></i> Architecture Design
                <p>perancangan software yang terbuka agar dapat diterapkan 
                   sistem pembangunan per-bagian pada tahapan selanjutnya.
                </p>
              </li>
              <li><i class="ri-check-double-line"></i> Code
                <p> Melakukan koding </p>
              </li>
              <li><i class="ri-check-double-line"></i> Test
                <p>melakukan testing dalam model ini.</p>
              </li>
            </ul>
            <p>
              <b>Kekurangan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Kemungkinan tiap bagian tidak dapat diintegrasikan.</li>
              <li><i class="ri-check-double-line"></i> Dapat menjadi build and fix model, karena kemampuannya untuk selalu mendapat perubahan selama proses rekayasa berlangsung.</li>
              <li><i class="ri-check-double-line"></i> Harus Open Architecture.</li>
              <li><i class="ri-check-double-line"></i> Mungkin terjadi kesulitan untuk memetakan kebutuhan pengguna ke dalam rencana spesifikasi masing-masing hasil increment.</li>
              <li><i class="ri-check-double-line"></i> Membutuhkan waktu yang relative lama untuk menghasilan product yang lengkap.</li>
            </ul>
            <p>
              <b>Metodologi Incremental cocok digunakan ketika</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> System dengan interaksi skala kecil dan medium.</li>
              <li><i class="ri-check-double-line"></i> System dengan masa penggunaan jangka pendek.</li>
              <li><i class="ri-check-double-line"></i> Cocok untuk proyek berukuran kecil (tidal lebih dari 200.000 coding).</li>
              <li><i class="ri-check-double-line"></i> Sistem terus berkembang.</li>
            </ul>
