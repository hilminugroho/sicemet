    <section id="about" class="about">
      <div class="container" data-aos="fade-up">

        <div class="section-title">
          <h2>Apa itu Metodologi Scrum?</h2>
        </div>
        <div class="col-lg-6 order-1 order-lg-2 hero-img" data-aos="zoom-in" data-aos-delay="200" style="margin-left: auto; margin-right: auto; display: block;">
          <img src="assets/img/model/alur_scrum.png" class="img-fluid animated" alt="">
        </div>
        <hr>
        <div class="row content">
          <div class="col-lg-6 pt-4 pt-lg-0">

            <p> <b>Definisi</b>
            </p>
            <p>
            Scrum bukanlah sebuah metodologi, melainkan sebuah framework (batasan dan aturan yang perlu dipenuhi untuk mencapai sesuatu sedangkan cara, proses, dan tekniknya dapat bebas berkreasi) atau kerangka kerja yang digunakan untuk mengembangkan produk kompleks.</p>
            <p>Scrum adalah sebuah kerangka kerja, sehingga di dalamnya terdapat mini tim untuk mensukseskan misinya. Scrum team terdiri dari 6-10 orang, terdiri dari product owner, development team, dan scrum master. Scrum Team sangat fleksibel dan mampu beradaptasi, sesuai dengan kemampuan dan etos kerja tim. Tim ini mengelola pekerjaannya sendiri, maksudnya tim ini tidak diperintah oleh orang lain di luar Scrum team. Mereka bertugas untuk memaksimalkan peluang yang ada serta menggali potensi pasar, sehingga Scrum Team dapat memproduksi sekaligus menghantarkan suatu produk dengan mendapatkan umpan balik dari masyarakat. 
            </p>
            <p>
              <b>Kelebihan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Lebih banyak transparansi dan visibilitas proyek.</li>
              <li><i class="ri-check-double-line"></i> Meningkatkan akuntabilitas tim.</li>
              <li><i class="ri-check-double-line"></i> Mudah mengakomodasi perubahan.</li>
              <li><i class="ri-check-double-line"></i> Peningkatan penghematan biaya.</li>
              <li><i class="ri-check-double-line"></i> Kebutuhan yang tidak stabil tidak menghambat proyek.</li>
            </ul>
            <p>
              <b>Kekurangan</b>
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i>Risiko scope creep
                <p>Beberapa proyek Scrum dapat mengalami scope creep karena kurangnya batas waktu/dateline. Tanpa batas waktu tersebut, stakeholder mungkin akan tergoda untuk terus meminta fungsi tambahan.</p>
              </li>
              <li><i class="ri-check-double-line"></i>Tim membutuhkan pengalaman dan komitmen
                <p>Dengan peran dan tanggung jawab yang ditetapkan, tim perlu menjalin rasa keakraban satu sama lain. Karena dalam Tim Scrum tidak ada peran didefinisikan (setiap orang melakukan sesuatu), yang membagi tugas anggota tim secara teknisnya. Tim Scrum juga perlu berkomitmen untuk melakukan planning meeting dan tinggal di tim selama proyek.</p>
              </li>
              <li><i class="ri-check-double-line"></i>Kesalahan Scrum Master dapat merusak segalanya
                <p>Beberapa proyek Scrum dapat mengalami scope creep karena kurangnya batas waktu/dateline. Tanpa batas waktu tersebut, stakeholder mungkin akan tergoda untuk terus meminta fungsi tambahan.</p>
              </li>
              <li><i class="ri-check-double-line"></i>Buruknya definisi tugas dapat menyebabkan ketidakakuratan
                <p>Jika tugas tidak didefinisikan dengan baik, perkirakan biaya proyek dan waktu tidak akan akurat. Dalam kasus seperti ini, tugas dapat tersebar di beberapa sprint.</p>
              </li>
              <li><i class="ri-check-double-line"></i>Waktu akhir proyek tidak jelas
              </li>
              <li><i class="ri-check-double-line"></i>Selalu muncul fitur baru
              </li>
              <li><i class="ri-check-double-line"></i>Perkiraan cost dan waktu tidak bisa akurat
              </li>
            </ul>
          </div>
           <div class="col-lg-6">
            <p>
              <b>Tahapan pada model Scrum</b> 
            </p>
            <ul>
              <li><i class="ri-check-double-line"></i> Product Owner
                <p>Mendata seluruh permintaan terhadap produknya sesuai prioritasnya.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Scrum Team
                <p>Memilih salah satu pekerjaan atau item dari list prioritas  Product Owner. Dalam tahap ini Scrum Team menganalisis, menggali, serta merencanakan alur kerja mereka dalam menyelesaikan pekerjaan ini.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Scrum Team
                <p>Memastikan seluruh Scrum team bekerja sesuai rencana awal.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Scrum Master
                <p> Pada tahapan ini akan dibuat rancangan software secara lebih terinci sesuai spesifikasi yang sudah disepakati.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Di Akhir
                <p>Waktu yang telah disediakan (sprint) atau ketika Developer Team telah menyelesaikan pekerjaannya, maka pekerjaan harus sudah siap untuk dikirim.</p>
              </li>
              <li><i class="ri-check-double-line"></i> Review Pekerjaan Scrum Team (review product)
              </li>
              <li><i class="ri-check-double-line"></i> Proyek dianggap selesai
                <p>Baik karena kualitas produk telah sesuai permintaan, atau karena pekerjaan sudah berada pada batas waktu yang ditentukan.</p>
              </li>
            </ul>
            <p>
              <b>Metodologi Scrum cocok digunakan ketika</b>
            </p>
            <ul>
            <li><i class="ri-check-double-line"></i> Estimasi waktu pengerjaan proyek tidak memiliki deadline yang cepat.</li>
            <li><i class="ri-check-double-line"></i> Tidak memiliki pekerjaan rutin, yang banyak memunculkan backlog baru di tengah-tengah sprint akan berakibat mengganggu jalannya sprint.</li>
          </ul>
